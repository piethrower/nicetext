#!/usr/bin/env node
// build-monotyped-models.js: per-corpus monotyped-model precompute
// for Eve.
//
// Produces, for every unique corpus referenced by a card in
// fixtures/cards.data.js, a single packed-SAB file containing the
// corpus's monotyped model:
//
//   fixtures/<corpus>.monotyped-model.sab.gz
//
// The SAB carries both:
//   - the unique sorted set of monotyped sentences (binary-searchable
//     via wrapMonotypedModel.hasSorted), and
//   - the corpus-order index (positional .at(i) over those unique
//     sentences with no string duplication).
//
// Format: NTMM (see js/src/eve/monotyped-model-sab.js). One file
// per corpus; consumed at runtime via
// loadResource(stem, 'monotyped-model', { fixture: true }).
//
// Driven from fixtures/cards.data.js so the corpus set stays in
// sync with what Eve actually loads at runtime. Multiple cards may
// share a corpus; the precompute is keyed by corpus file, so shared
// corpora compute once.
//
// Eve's corpus-vocab precompute is NOT produced here; it lives at
// /fixtures/<stem>.wlist.sab.gz, generated by build-corpus-wlist.js
// + `sab pack wlist`. Eve consumes it via loadResource(stem, 'wlist').
//
// Usage:  node tools/build-monotyped-models.js

import { writeFileSync } from 'node:fs';
import { gzipSync, constants as zlibConstants } from 'node:zlib';
import { fileURLToPath } from 'node:url';
import { dirname, join, basename } from 'node:path';

import cardsRegistry from '../fixtures/cards.data.js';
import { genMonotypedModel } from '../js/src/eve/monotyped-model-check.js';
import { loadCorpusText } from './load-corpus.js';
import { repoPath } from './byos-build-helpers.js';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const FIXTURES_DIR = join(ROOT, 'fixtures');
const GZ_LEVEL = { level: zlibConstants.Z_BEST_COMPRESSION };

// Derive the per-corpus output stem from a corpus path like
// 'fixture-src/texts/aesop.txt' or 'fixture-src/texts/texting-teen*.txt'.
// Mirrors the stem half of getCorpusFile in js/src/byos.js: strip
// directory, strip '*', strip '.txt'.
function corpusStem(corpusPath) {
  const base = corpusPath.split('/').pop();
  return base.replace(/\*/g, '').replace(/\.txt$/i, '');
}

// Dedupe cards by corpus path. Aesop and any future aesop-variant
// cards sharing fixture-src/texts/aesop.txt share one precompute.
const uniqueCorpora = new Map();
for (const card of cardsRegistry) {
  const path = card.build && card.build.corpus;
  if (!path) continue;
  if (!uniqueCorpora.has(path)) uniqueCorpora.set(path, corpusStem(path));
}

process.stderr.write(`Eve monotyped-model precomputes: ${uniqueCorpora.size} unique corpora\n`);

let producedCount = 0;
let totalBytesIn = 0;
let totalBytesOut = 0;

function sabToBuffer(ab) {
  return Buffer.from(new Uint8Array(ab));
}

// Each corpus produces two files in one pass over the source text:
//   <stem>.monotyped-model.sab.gz          : baseline (case markers kept)
//   <stem>-casefree.monotyped-model.sab.gz : case-marker puncts stripped,
//                                             matches a case-reformatted cover.
// The casefree variant uses the stem-suffix path (still 'monotyped-model'
// SAB category) so loadResource resolves it as
// loadResource(`${stem}-casefree`, 'monotyped-model', { fixture: true }).
for (const [corpusPath, stem] of uniqueCorpora) {
  const srcPath = repoPath(corpusPath);
  const text = loadCorpusText(srcPath);
  for (const variant of [
    { suffix: '',          casefree: false, label: 'baseline' },
    { suffix: '-casefree', casefree: true,  label: 'casefree' },
  ]) {
    const built = await genMonotypedModel(text, { casefree: variant.casefree });
    const buf = sabToBuffer(built.sab);
    const gz = gzipSync(buf, GZ_LEVEL);
    const dst = join(FIXTURES_DIR, `${stem}${variant.suffix}.monotyped-model.sab.gz`);
    writeFileSync(dst, gz);
    producedCount++;
    totalBytesIn += buf.length;
    totalBytesOut += gz.length;
    process.stderr.write(
      `  ${basename(dst)}  ${gz.length.toLocaleString()} bytes gz ` +
      `(${buf.length.toLocaleString()} raw, ` +
      `${built.count.toLocaleString()} sentences, ` +
      `${built.uniqueCount.toLocaleString()} unique MM, ` +
      `${built.cmmUniqueCount.toLocaleString()} unique CMM, ${variant.label})\n`,
    );
  }
}

process.stderr.write(
  `\nEve monotyped-model precomputes: ${producedCount} files, ` +
  `${totalBytesIn.toLocaleString()} raw bytes -> ` +
  `${totalBytesOut.toLocaleString()} gz bytes ` +
  `(${(100 * totalBytesOut / totalBytesIn).toFixed(1)}% ratio)\n`,
);

// loadResource (called transitively via genMonotypedModel →
// generateModelTable → getRedactedMatcher) uses a worker_threads
// pool that keeps the event loop open after work completes. Explicit
// exit so the CLI returns promptly.
process.exit(0);
