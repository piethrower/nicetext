// Non-breaking space; keeps a number and its unit on the same line.
const NB = ' ';

function formatBytes(n) {
  if (n >= 1024 * 1024) return `${(n / 1024 / 1024).toFixed(2)}${NB}MB`;
  if (n >= 1024) return `${(n / 1024).toFixed(1)}${NB}KB`;
  return `${n.toLocaleString()}${NB}bytes`;
}

// Render the stats as an inline natural-language run with each
// number/unit pair held together by a non-breaking space, separated by
// middle dots that the line MAY break on. Replaces the prior <dl>
// grid.
function renderPhrasesInto(container, phrases) {
  container.replaceChildren();
  for (let i = 0; i < phrases.length; i++) {
    if (i > 0) {
      const sep = document.createElement('span');
      sep.className = 'stats-sep';
      sep.textContent = ' · ';
      container.appendChild(sep);
    }
    const span = document.createElement('span');
    span.className = 'stats-pair';
    span.textContent = phrases[i];
    container.appendChild(span);
  }
}

function renderStatsInto(container, stats) {
  renderPhrasesInto(container, [
    `${stats.wordCount.toLocaleString()}${NB}words in ${stats.typeCount.toLocaleString()}${NB}types`,
    `${stats.avgBits.toFixed(2)}${NB}avg bits / word`,
    `${stats.maxBits}${NB}max bits`,
    formatBytes(stats.sabBytes),
  ]);
}

function modelStatsPhrases(stats) {
  const out = [
    `${stats.modelCount.toLocaleString()}${NB}models`,
  ];
  if (stats.dynamicModels != null) {
    out.push(
      `${stats.dynamicModels.toLocaleString()}${NB}dynamic`,
      `${stats.staticModels.toLocaleString()}${NB}static`,
    );
  }
  out.push(
    `${stats.totalSentences.toLocaleString()}${NB}sentences`,
    `${stats.avgLength.toFixed(1)}${NB}avg length`,
    `${stats.maxLength}${NB}max`,
    formatBytes(stats.sabBytes),
  );
  return out;
}

function grammarStatsPhrases(stats) {
  return [
    `${stats.ruleCount.toLocaleString()}${NB}rule${stats.ruleCount === 1 ? '' : 's'}`,
    `${stats.altCount.toLocaleString()}${NB}alternative${stats.altCount === 1 ? '' : 's'}`,
    `${stats.maxTokens}${NB}max${NB}tokens`,
    formatBytes(stats.sabBytes),
  ];
}

function flatStatsPhrases() {
  return [`0${NB}models`, `0${NB}KB${NB}(Flat)`];
}

