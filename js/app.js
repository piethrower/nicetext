// Tabs (Your Secret / Your Style / Your Cover Story). Mutually
// exclusive body class (tab-secret / tab-style / tab-cover) drives
// which panel is visible (see css/nicetext.css §nav.tabs). Selection
// persists across reloads in the same browser session via cookie.
// Mirrors the cookie pattern used by tutorial-script.js / theme.js.
const TAB_COOKIE = 'nicetext-active-tab';
const TAB_NAMES = ['secret', 'style', 'cover', 'eve'];
function readTabCookie() {
  for (const part of document.cookie.split(';')) {
    const [k, v] = part.trim().split('=');
    if (k === TAB_COOKIE) {
      const val = decodeURIComponent(v || '');
      if (TAB_NAMES.includes(val)) return val;
    }
  }
  return 'style';
}
function writeTabCookie(name) {
  document.cookie = `${TAB_COOKIE}=${encodeURIComponent(name)}; path=/; SameSite=Lax`;
}
function setActiveTab(name) {
  if (!TAB_NAMES.includes(name)) return;
  for (const n of TAB_NAMES) {
    document.body.classList.toggle(`tab-${n}`, n === name);
  }
  for (const btn of document.querySelectorAll('nav.tabs button[role="tab"]')) {
    btn.setAttribute('aria-selected', String(btn.dataset.tab === name));
  }
  writeTabCookie(name);
}
for (const btn of document.querySelectorAll('nav.tabs button[role="tab"]')) {
  btn.addEventListener('click', () => setActiveTab(btn.dataset.tab));
}
