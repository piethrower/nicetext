// Decode the upload as UTF-8 with replacement (fatal=false). The
// engine lexer ignores anything outside its letter/digit/punct range,
// so binary noise or stray U+FFFD replacement chars drop on the floor,
// matches the developer's "just pull the strings and punctuation"
// intent.
// SHA-256 prefix (first 8 bytes = 16 hex chars) so the share URL can
// include a "did you upload the right corpus?" check without bloating
// the URL with a 64-char hash. Birthday collisions at ~4B distinct
// inputs (fine for non-adversarial accidental-mismatch detection).
async function corpusHashPrefix(text) {
  try {
    const buf = enc.encode(text);
    const digest = await crypto.subtle.digest('SHA-256', buf);
    return [...new Uint8Array(digest).slice(0, 8)]
      .map(b => b.toString(16).padStart(2, '0')).join('');
  } catch {
    return '';
  }
}

// Recompute customCorpus from the textarea. Call after any path that
// mutates the textarea (typing, paste, file load, clear). State is set
// synchronously so the validation row sees the new corpus immediately;
// the hash arrives a tick later and the share-cover URL reads it from
// state at click time, never racing it.
function commitCustomCorpus({ name, fromFile }) {
  const text = $('adv-custom-text').value;
  if (!text) {
    customCorpus = null;
    refreshAdvancedValidation();
    return;
  }
  const keepName = name ?? customCorpus?.name ?? '(typed)';
  const keepFromFile = fromFile ?? customCorpus?.fromFile ?? false;
  customCorpus = { name: keepName, text, hash: '', fromFile: keepFromFile };
  refreshAdvancedValidation();
  // Returns the hash settle promise so callers that need the hash
  // before proceeding can await it; the input listener still
  // fire-and-forgets since typing races itself.
  return corpusHashPrefix(text).then(h => {
    if (customCorpus && customCorpus.text === text) {
      customCorpus.hash = h;
      refreshAdvancedValidation();
    }
  });
}

// Dirty-on-input flag, flipped by the textarea input listener and
// cleared by the verify pass below. Used to skip the verify modal
// when the developer blurs without having typed anything (or when
// the verify has already run since the last edit).
let corpusDirty = false;

// Verify pass: open progress modal, run precleanCorpus on the
// textarea content, rewrite the textarea if any rule fired, then
// recommit state. scrollTop and selection are preserved across
// the write-back so the developer's eye lands where it was.
//
// Triggered from paste, file load, and blur-after-edit. Typing alone
// does NOT trigger this, that would pop a modal on every keystroke.
// Optional `text` parameter: when provided (file load, paste), the
// caller passes the raw text directly so we skip the
// load-into-textarea-then-read-back round-trip. That round-trip is
// expensive at MB scale because writing N MB into the textarea
// triggers a deferred layout/paint that fires on the next sync
// reflow (e.g., the dialog showModal call below), blocking the
// main thread before the worker even gets the message. Blur path
// still passes no text (the textarea content IS the source).
// `existingModal` (optional): caller has already opened a progress
// modal (e.g., the file-load handler that surfaces the read+decode
// phases: audit 2026-05-17 Finding 6). When supplied we reuse it
// rather than open a new one, so the user sees one continuous modal
// lifecycle. When omitted (paste / blur paths) we open our own
// "Cleaning Corpus" modal.
async function verifyAndPrecleanCustomCorpus({ name, fromFile, text, existingModal } = {}) {
  const ta = $('adv-custom-text');
  const before = text != null ? text : ta.value;
  if (!before) {
    corpusDirty = false;
    customCorpus = null;
    refreshAdvancedValidation();
    if (existingModal) { try { existingModal.close(); } catch {} }
    return;
  }
  const modal = existingModal || openProgressModal('Cleaning Corpus');
  const inLen = before.length;
  modal.update(`Normalizing text (${inLen.toLocaleString()} chars)...`, null);
  // The preclean runs in a worker (preclean-async.js), so the main
  // thread stays responsive even for multi-MB corpora that would
  // otherwise spend seconds in synchronous regex work. The rAF yield
  // ensures the modal paints before we await the worker's first
  // postMessage.
  await new Promise(r => requestAnimationFrame(r));

  let after;
  try {
    after = await precleanCorpusAsync(before, {
      signal: modal.signal,
      onProgress: (info) => {
        // Per-chunk update from the worker (audit Findings 4 + 5,
        // 2026-05-18). precleanCorpus now slices the input into
        // ~512 KB chunks and runs all 8 rules per chunk, posting one
        // update per chunk, keeps the modal's ≥1 Hz contract even
        // at 200 MB.
        const fmt = info.chars.toLocaleString();
        const fraction = info.chunkCount > 0
          ? (info.chunkIndex + 1) / info.chunkCount
          : null;
        modal.update(
          `Cleaning corpus, pass ${info.pass}, chunk ${info.chunkIndex + 1} of ${info.chunkCount}: ${fmt} chars`,
          fraction,
        );
      },
    });
  } catch (err) {
    if (err && (err.name === 'AbortError' || err.code === 'cancelled')) {
      // User clicked Cancel, close the modal cleanly and leave the
      // textarea however we found it (file load path: empty; blur
      // path: unchanged).
      try { modal.close(); } catch {}
      return;
    }
    modal.showError(`Preclean failed: ${err && err.message ? err.message : err}`);
    return;
  }

  // Write to the textarea when:
  //   - caller passed `text` (file load / paste): textarea hasn't seen
  //     this content yet, write the cleaned version.
  //   - blur path (no `text` arg) and preclean changed something:
  //     update the textarea, preserving scroll + selection.
  // No-op when blur saw nothing to clean.
  const needsWrite = text != null || after !== before;
  if (needsWrite) {
    // ta.value = bigText is a synchronous DOM op that can block the
    // main thread on Safari for multi-MB strings. Surface progress
    // and yield to paint so the user knows what's happening AND can
    // still see Cancel land cleanly (the abort path was reachable
    // up through precleanCorpusAsync).
    modal.update(`Updating display (${after.length.toLocaleString()} chars)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    const scrollTop = ta.scrollTop;
    const selStart = ta.selectionStart;
    const selEnd = ta.selectionEnd;
    ta.value = after;
    ta.scrollTop = scrollTop;
    try { ta.setSelectionRange(selStart, selEnd); } catch {}
    await new Promise(r => requestAnimationFrame(r));
  }

  modal.update(`Computing corpus hash...`, null);
  await new Promise(r => requestAnimationFrame(r));
  corpusDirty = false;
  await commitCustomCorpus({ name, fromFile });

  // Auto-dismiss when there's nothing to tell the developer about.
  // If preclean didn't change anything, the modal is just noise.
  if (after === before) {
    modal.close();
    return;
  }
  modal.update(
    'Some characters were normalized for the lexer ' +
    '(smart quotes, exotic spaces, mixed-script confusables, &c.). ' +
    'The textarea has been updated.',
    1
  );
  modal.done();
}

$('adv-custom-file').addEventListener('change', async (ev) => {
  const f = ev.target.files?.[0];
  if (!f) return;
  // Open the modal BEFORE the file-read/decode awaits so the user gets
  // immediate UI feedback. Otherwise these two awaits create a multi-
  // second dead window on large files (audit 2026-05-17 Finding 6:
  // 2.9 s on 200 MB chromium, 4.0 s on firefox). The same modal
  // continues through preclean, we pass it to
  // verifyAndPrecleanCustomCorpus as `existingModal` so there's no
  // open/close flicker between the read and clean phases.
  //
  // Cancel during the read+decode window is decorative: neither
  // `File.arrayBuffer()` nor synchronous `TextDecoder.decode()` accept
  // an AbortSignal. The modal at least confirms the click registered.
  const modal = openProgressModal('Loading Sample Text');
  modal.update(`Reading file (${f.size.toLocaleString()} bytes)...`, null);
  await new Promise(r => requestAnimationFrame(r));
  try {
    const buf = new Uint8Array(await f.arrayBuffer());
    if (modal.signal.aborted) { try { modal.close(); } catch {} ev.target.value = ''; return; }
    modal.update(`Decoding UTF-8 (${buf.byteLength.toLocaleString()} bytes)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    const text = new TextDecoder('utf-8', { fatal: false }).decode(buf);
    if (modal.signal.aborted) { try { modal.close(); } catch {} ev.target.value = ''; return; }
    // Pass the raw text directly; verify writes the cleaned version
    // to the textarea once preclean returns. Skipping the
    // load-into-textarea-then-read-back round-trip avoids a multi-MB
    // deferred layout/paint that fires on the next sync reflow.
    await verifyAndPrecleanCustomCorpus({
      name: f.name, fromFile: true, text, existingModal: modal,
    });
  } catch (err) {
    try { modal.showError(`Couldn't read file: ${err?.message || err}`); } catch {}
    $('adv-custom-text').value = '';
    customCorpus = null;
    refreshAdvancedValidation();
    showPageStatus('error', 'Sample text', `couldn't read that file, ${err?.message || err}`, 'Try a different sample-text file.');
  }
  // Reset the input so re-selecting the same file fires 'change' again.
  ev.target.value = '';
});

