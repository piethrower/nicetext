// Detect which preset matches the current emoji-related state. Returns
// 'full-control' when no preset bundle matches (i.e. the developer has
// stepped past presets into specific per-row choices).
function detectEmojiPreset(state) {
  for (const k of ['none', 'sprinkle', 'flood']) {
    const p = EMOJI_PRESETS[k];
    const srcMatch = EMOJI_SOURCES.every(s =>
      state.sources.includes(s) === p.sources.includes(s));
    const augMatch = state.eiwEnabled === p.eiwEnabled
      && state.wieEnabled === p.wieEnabled
      && (!p.eiwEnabled || state.eiwIntensity === p.eiwIntensity)
      && (!p.wieEnabled || state.wieIntensity === p.wieIntensity);
    if (srcMatch && augMatch) return k;
  }
  return 'full-control';
}

// Apply a preset to the DOM: write each codebook checkbox + aug
// enable + slider value. Fires `change` so the panel-validation +
// chip-highlight pipelines re-read the new state.
function applyEmojiPreset(presetKey) {
  if (presetKey === 'full-control') return;
  const p = EMOJI_PRESETS[presetKey];
  for (const s of EMOJI_SOURCES) {
    const cb = document.getElementById(`adv-src-${s}`);
    if (!cb) continue;
    cb.checked = p.sources.includes(s);
    cb.dispatchEvent(new Event('change', { bubbles: true }));
  }
  const writeAug = (id, enabled, intensity) => {
    const cb = document.getElementById(id);
    if (cb) {
      cb.checked = enabled;
      cb.dispatchEvent(new Event('change', { bubbles: true }));
    }
    const slider = document.getElementById(`${id}-intensity`);
    const readout = document.getElementById(`${id}-readout`);
    if (slider) {
      slider.value = String(intensity);
      slider.dispatchEvent(new Event('input', { bubbles: true }));
    }
    if (readout) readout.textContent = String(intensity);
  };
  writeAug('adv-eiw', p.eiwEnabled, p.eiwIntensity);
  writeAug('adv-wie', p.wieEnabled, p.wieIntensity);
}

// Update the pillbox's aria-pressed state for the active preset, and
