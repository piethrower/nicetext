// language. Anything not matched falls through to a generic message.
function friendlyError(err) {
  const m = (err && err.message) || String(err || 'unknown error');
  // Encoder + the user's chosen style/dict pair can't make payload
  // progress: either the style references types absent from the dict,
  // or the dict has too few multi-word types.
  if (/models picked without consuming any bits/.test(m) ||
      /no models in this table contain any bit-bearing type slots/.test(m) ||
      /typeIndex \d+ not in dictionary/.test(m)) {
    // Corpus-specific advice when the active recipe is custom: the dict
    // is fine (often the master), the corpus is just too narrow. The
    // build pipeline catches this earlier with a clearer message; this
    // branch is a safety net for the conceal/reveal paths that hit the
    // raw stream-time throw against a stale-but-still-installed bad
    // selection.
    if (currentSelection?.recipe?.kind === 'advanced'
        && currentSelection?.recipe?.story === 'custom') {
      return "Your corpus is too short or too repetitive to encode a " +
        "secret. Try a longer or more varied corpus, then click Build again.";
    }
    const vocabRestricted = currentSelection?.recipe?.kind === 'advanced'
      && $('adv-vocab-toggle')?.value === 'corpus';
    if (vocabRestricted) {
      return "This Story Style and Decoder Dictionary don't work together. " +
        "Turn on “Vocabulary: Expand to include codebook words” in the " +
        "Advanced panel, or pick a different style.";
    }
    return "This Story Style and Decoder Dictionary don't work together. " +
      "Pick a different style, or switch to “* All English words”.";
  }
  if (/no encoding types in dictionary/.test(m)) {
    const vocabRestricted = currentSelection?.recipe?.kind === 'advanced'
      && $('adv-vocab-toggle')?.value === 'corpus';
    if (vocabRestricted) {
      return "That Decoder Dictionary doesn't have enough variety to smuggle " +
        "a secret. Turn on “Vocabulary: Expand to include codebook words” in " +
        "the Advanced panel, or pick a different style.";
    }
    return "That Decoder Dictionary doesn't have enough variety to smuggle a " +
      "secret. Try “* All English words”.";
  }
  if (/walked \d+ bits for type \d+ without hitting a leaf/.test(m)) {
    return "Something looks off with that Decoder Dictionary. Reload the " +
      "page, or pick a different one.";
  }
  if (/unsupported dictionary version/.test(m)) {
    return "That Decoder Dictionary is in a format this page can't read. " +
      "Reload the page.";
  }
  if (/model table missing typeNames/.test(m)) {
    return "That Story Style is in an old format. Reload the page, or " +
      "pick a different style.";
  }
  if (/empty model table/.test(m)) {
    return "That Story Style is empty. Pick a different style.";
  }
  if (/expand: unknown rule/.test(m)) {
    return "Something looks off with that Story Style. Pick a different one.";
  }
  if (/could not generate a model under maxLength/.test(m)) {
    return "This Story Style needs more room than the page allows. Pick a " +
      "different style.";
  }
  if (/^fetch .+ HTTP \d+/.test(m)) {
    return "Couldn't load that Story Style or Decoder Dictionary. Reload " +
      "the page.";
  }
  if (err && (err.name === 'QuotaExceededError' || /quota.*exceeded/i.test(m))) {
    return "Not enough free disk space to save the file. Free up space, or " +
      "try a smaller secret.";
  }
  if (/round-trip validation failed/.test(m)) {
    return "ERROR: the generated text might be entertaining, but due to a " +
      "bug, it does NOT recover the secret. Try again, or pick a different style.";
  }
  if (err && (err.code === 'cancelled' || err.name === 'AbortError')) {
    return 'Cancelled.';
  }
  console.error('Unhandled error surfaced to user:', err);
  return "Something went wrong. Reload the page, or pick a different style.";
}

// ---- Progress modal driver ----
// The modal opens immediately so super-quick jobs still register. On
// success the caller calls modal.done(): Cancel swaps to Done, the
// button flashes on-off-on-off-on (900 ms) and stays highlighted until
// the developer clicks Done. No auto-close, the modal sits there
// inviting inspection of the final byte count. Error and cancel paths
// call modal.close() directly for an immediate dismiss with no Done
// state.

// openProgressModal is the Build / Conceal / Reveal / file-load /
// custom-build progress modal. It used to be a hand-rolled driver;
// it now delegates to a singleton createProgressModal instance
// (_buildProgressModal, initialized below) and only adds the three
// Build-specific row patterns the factory doesn't know about
// Engine aug-kind codes → human labels for progress-modal rows.
// The codes (eiw / wie / vowel) are byos-schema identifiers used
// across the engine, worker, and on-disk fixtures, kept as-is for
// stable IDs. Only the UI rendering is humanized.
const AUG_KIND_LABEL = {
  eiw: 'emoji into words',
  wie: 'words into emoji',
  vowel: 'vowel',
};

// (updateAugs per-worker rows, updateValidate self-check row,
// enableSkipValidation button-on-validate-row).
//
// The signature stays `(headerLabel) => { signal, rows, update, ... }`
// so all 12 existing callsites keep working without edits. update()
// keeps the legacy `(detail, _fraction)` shape; everything else
// (showError, done, updateEta, etc.) lives on the factory.
function openProgressModal(headerLabel) {
  _buildProgressModal.open(headerLabel);
  const rows = _buildProgressModal.rows;
  return {
    // Forwarders. signal is a getter on the factory; expose as a
    // plain property here because callers read it once and pass it
    // around (e.g., to the worker job options).
    signal: _buildProgressModal.signal,
    rows,
    // Legacy update(detail, _fraction). Fraction is accepted for
    // call-site compatibility but ignored at the bar; callers that
    // want a visible % should embed it in detail.
    update(detailText, _fraction) {
      _buildProgressModal.update({ detail: detailText });
    },
    updateEta(text)   { _buildProgressModal.updateEta(text); },
    showError(text)   { _buildProgressModal.showError(text); },
    done()            { _buildProgressModal.done(); },
    close()           { _buildProgressModal.close(); },

    // Round-trip self-check progress (encode path only). Renders as
    // a 'validate' row in the unified row block; first call
    // materializes the row, subsequent calls update its label.
    // fraction (when present) is rendered as a percent suffix
    // because per-row determinate bars are out.
    updateValidate(detailText, fraction) {
      const pctText = (fraction != null && Number.isFinite(fraction))
        ? ` (${Math.round(fraction * 100)}%)`
        : '';
      rows.add('validate', `${detailText}${pctText}`);
    },
    // Wire the per-job Skip Verification button. Attaches a button
    // to the 'validate' row; one-shot, first click detaches the
    // validator and disables the button. Calling before
    // updateValidate first runs is fine; the row materializes with
    // a placeholder label until the validator emits.
    enableSkipValidation(onClick) {
      const skipBtn = document.createElement('button');
      skipBtn.type = 'button';
      skipBtn.className = 'progress-row-skip';
      skipBtn.textContent = 'Skip verification';
      let used = false;
      skipBtn.addEventListener('click', () => {
        if (used) return;
        used = true;
        skipBtn.disabled = true;
        rows.update('validate', 'Verification skipped, finishing encode...');
        try { onClick(); } catch {}
      });
      // Pass null label so we don't stomp the prior updateValidate
      // text. If the row doesn't yet exist (enableSkipValidation
      // fired before updateValidate), the placeholder "loading"
      // appears briefly until the first onValidateProgress.
      rows.add('validate', null, { control: skipBtn });
    },
    // Per-worker aug snapshot delivered by the build-session worker.
    // `state` shape: { iter, cap, poolSize, augKinds,
    //                  workers: [{workerId, augKind, emitted, status}, ...] }.
    // Renders the iter header as its own row (id 'aug-iter') plus
    // one row per (workerId, augKind) pair keyed by
    // 'aug:w${id}:${kind}'. Workers that fall out of state.workers
    // between calls are NOT removed, the legacy modal preserved
    // the final per-worker counts so the developer can read what
    // each aug produced.
    updateAugs(state) {
      if (!state) return;
      const cap = state.cap ? ` / max ${state.cap}` : '';
      const pool = state.poolSize ? ` · pool ${state.poolSize}` : '';
      rows.add('aug-iter', `Augs iter ${state.iter}${cap}${pool}`);
      for (const w of (state.workers || [])) {
        const tail = w.status === 'done' ? ' done' : '';
        const kind = AUG_KIND_LABEL[w.augKind] || w.augKind;
        const label = `worker ${w.workerId} · ${kind}: ${(w.emitted || 0).toLocaleString()} emitted${tail}`;
        rows.add(`aug:w${w.workerId}:${w.augKind}`, label);
      }
    },
  };
}

function bytesLooksLikeUtf8(bytes) {
  try { dec.decode(bytes); return true; } catch { return false; }
}

function downloadBytes(bytes, filename, mime = 'application/octet-stream') {
  const blob = new Blob([bytes], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click(); document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}
