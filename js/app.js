function selectChip(chipId) {
  const chip = CHIPS.find(c => c.id === chipId);
  if (!chip || chip.kind === 'modal') return;
  currentSelection = chipSelectionFor(chip);
  advancedActive = false;
  // Style change invalidates the cover↔style consistency claim.
  // Share pill goes back to disabled until the user re-runs Conceal
  // or Reveal with the new style and the result lands cleanly.
  if (typeof coverStyleConsistent !== 'undefined') {
    coverStyleConsistent = false;
    if (typeof refreshShareEnabled === 'function') refreshShareEnabled();
  }
  updateStyleMeta();
  // Phase 2 observability: surface which cards back this chip's
  // fixtures. The dict and model can resolve to different cards
  // (e.g., jfk-ordered chips share the jfk dict but use the
  // jfk-ordered model). Logging only -- no behavior change.
  let dictCard = null;
  try {
    const dictEntry = DICTIONARIES.find(d => d.id === chip.dictId);
    const styleEntry = STYLE_SOURCES.find(s => s.id === chip.styleId);
    dictCard = dictEntry ? findCardByName(dictEntry.cardName, cardsRegistry) : null;
    const modelCard = styleEntry?.kind === 'model-table'
      ? findCardByName(styleEntry.cardName, cardsRegistry)
      : null;
    const parts = [];
    if (dictCard) parts.push(`dict→"${getBYOSID(dictCard, cardsRegistry)}"`);
    if (modelCard) parts.push(`model→"${getBYOSID(modelCard, cardsRegistry)}"`);
    if (parts.length) {
      console.info(`byos: chip "${chip.id}" resolves to ${parts.join(', ')}`);
    }
    // Track byosIDs as precomputed so the Build button reads "Built"
    // when the panel matches them. We use the long-form byosID
    // (generateBYOSID over the card object) since that's what
    // isByosIDPrecomputed looks up.
    if (dictCard?.byosID) loadedByosIDs.add(dictCard.byosID);
    if (modelCard?.byosID) loadedByosIDs.add(modelCard.byosID);
  } catch (e) {
    console.warn('byos: failed to resolve chip', e);
  }
  // Bidirectional binding: populate the Advanced panel from the chip's
  // backing card. Even when the panel is collapsed, its DOM state is
  // authoritative: opening it after a chip click reveals the synced
  // controls. Pure DOM write; no change events fire, so no re-entrant
  // selectChip cascade. The dictCard's byos has both story and base
  // blocks (post the byos.json honesty pass), so this is a complete
  // panel populate.
  if (dictCard) applyByosToPanel(dictCard);
  reflectSelectionInChips();
  refreshStatsPanel();
  markEncodeDirty();
  refreshDecodeButton();
}

// Highlight the chip that matches currentSelection. Curated chips are
// matched by the (dict path, style path) pair from currentSelection;
// the special Advanced chip lights up whenever a session build is
// active. Path comparisons guard against `undefined === undefined`
// matching the path-less Flat entry for any model-table chip.
function reflectSelectionInChips() {
  let selectedId = null;
  if (advancedActive) {
    selectedId = 'advanced';
  } else {
    const dictId = DICTIONARIES.find(d => dictPathFor(d.id) === currentSelection.dictPath)?.id;
    const styleEntry = STYLE_SOURCES.find(s =>
      (currentSelection.modelPath   && s.kind === 'model-table' && modelPathFor(s.id) === currentSelection.modelPath) ||
      (currentSelection.grammarPath && s.kind === 'grammar' && s.path === currentSelection.grammarPath) ||
      (s.kind === 'flat' && !currentSelection.modelPath && !currentSelection.grammarPath)
    );
    selectedId = CHIPS.find(c => c.dictId === dictId && c.styleId === styleEntry?.id)?.id || null;
  }
  for (const btn of document.querySelectorAll('.style-chip')) {
    btn.classList.toggle('selected', selectedId ? btn.dataset.chipId === selectedId : false);
  }
}

