// The jobs API caches resource SABs in its own internal map; we cache
// the *wrapped* dict object here so per-keystroke calls to
// lookupWord don't re-wrap on each call. Wrap is cheap (just a few
// header reads) but wrapping once and reusing is cheaper still.
const dictWrapCache = new Map();
async function getLoadedDict(path) {
  const cached = dictWrapCache.get(path);
  if (cached) return cached;
  const promise = loadResource(path, 'dict').then(wrapDictionaryFromSAB);
  dictWrapCache.set(path, promise);
  promise.catch(() => dictWrapCache.delete(path));
  return promise;
}

