// Build an Enable/Disable pillbox bound to a hidden checkbox. The
// checkbox is the source of truth for byos serialization; the
// pillbox buttons mirror its state via aria-pressed and dispatch
// `change` on click so existing listeners on the checkbox keep
// firing. Programmatic `.checked` changes (e.g., applyByosToPanel
// → ev.dispatchEvent) also resync via a `change` listener.
function makePillToggle(cb, { onLabel = 'Enable', offLabel = 'Disable' } = {}) {
  const wrap = document.createElement('div');
  wrap.className = 'pill-toggle';
  wrap.setAttribute('role', 'group');
  const onBtn = document.createElement('button');
  onBtn.type = 'button';
  onBtn.dataset.pillValue = 'on';
  onBtn.textContent = onLabel;
  const offBtn = document.createElement('button');
  offBtn.type = 'button';
  offBtn.dataset.pillValue = 'off';
  offBtn.textContent = offLabel;
  wrap.append(onBtn, offBtn);
  const sync = () => {
    onBtn.setAttribute('aria-pressed',  cb.checked ? 'true'  : 'false');
    offBtn.setAttribute('aria-pressed', cb.checked ? 'false' : 'true' );
  };
  onBtn.addEventListener('click', () => {
    if (cb.checked) return;
    cb.checked = true;
    cb.dispatchEvent(new Event('change', { bubbles: true }));
    sync();
  });
  offBtn.addEventListener('click', () => {
    if (!cb.checked) return;
    cb.checked = false;
    cb.dispatchEvent(new Event('change', { bubbles: true }));
    sync();
  });
  cb.addEventListener('change', sync);
  sync();
  return wrap;
}

