// ---- Pipeline progress modal ----
// Same Eve-shape dialog as Build's progress modal, but each row is
// one file. State + progress + skip-verify button all flow through
// the unified row block; the per-row determinate bar is gone and
// each file's progress is conveyed by the row's text label
// (filename: status ...). Cancel becomes Done at batch end and the
// developer dismisses manually.
function openPipelineModal({ files, kind }) {
  const sel = currentSelection;
  const dlg = $('pipeline-modal');
  const titleEl = $('pipeline-modal-title');
  const testEl = $('pipeline-modal-test');
  const detailEl = $('pipeline-modal-detail');
  const barEl = $('pipeline-modal-bar');
  const cancelBtn = $('pipeline-modal-cancel');
  const closeXBtn = $('pipeline-modal-close');
  const rows = createLoadRowBlock('pipeline-modal-rows');

  titleEl.textContent = kind === 'encode'
    ? 'Concealing secrets or silliness into the cover story'
    : 'Revealing secrets or silliness from the cover story';
  testEl.textContent = kind === 'encode'
    ? `Processing ${files.length} file(s) using ${sel.dictLabel} / ${sel.styleLabel}.`
    : `Processing ${files.length} file(s) using ${sel.dictLabel}.`;
  detailEl.textContent = '';
  barEl.removeAttribute('value');
  cancelBtn.textContent = 'Cancel';
  cancelBtn.disabled = false;
  cancelBtn.classList.remove('is-flashing');
  rows.clear();

  // One row per file, keyed by 1-based index for the left-column
  // visible id. The filename and current status live in the label.
  const fileNames = files.map(f => f.name);
  for (let i = 0; i < files.length; i++) {
    rows.add(`${i + 1}`, `${fileNames[i]}: queued`);
  }

  const controller = new AbortController();
  let closed = false;
  const onCancelClick = () => {
    controller.abort();
    cancelBtn.disabled = true;
    cancelBtn.textContent = 'Cancelling...';
  };
  const onTopXClick = () => {
    if (closed) return;
    if (controller.signal.aborted) { finalClose(); return; }
    onCancelClick();
  };
  const onEscape = (ev) => { ev.preventDefault(); onTopXClick(); };
  cancelBtn.addEventListener('click', onCancelClick);
  closeXBtn.addEventListener('click', onTopXClick);
  dlg.addEventListener('cancel', onEscape);
  dlg.showModal();

  function finalClose() {
    if (closed) return;
    closed = true;
    cancelBtn.removeEventListener('click', onCancelClick);
    closeXBtn.removeEventListener('click', onTopXClick);
    dlg.removeEventListener('cancel', onEscape);
    rows.clear();
    if (dlg.open) dlg.close();
  }

  function rowId(idx) { return `${idx + 1}`; }
  function lblWith(idx, status) { return `${fileNames[idx]}: ${status}`; }

  return {
    signal: controller.signal,
    hooks: {
      onRowState(idx, state) {
        const txt = state === 'running' ? 'running...' : state;
        rows.add(rowId(idx), lblWith(idx, txt));
      },
      onRowProgress(idx, fraction, detail) {
        const pct = (fraction != null && Number.isFinite(fraction))
          ? ` (${Math.round(fraction * 100)}%)`
          : '';
        rows.update(rowId(idx), lblWith(idx, `${detail || 'running...'}${pct}`));
      },
      onRowSaved(idx, filename) {
        // Saved: drop any prior skip-verify button by passing
        // explicit control:null.
        rows.add(rowId(idx), lblWith(idx, `✓ saved as ${filename}`), { control: null });
      },
      onRowError(idx, msg) {
        rows.add(rowId(idx), lblWith(idx, `✗ failed: ${msg}`), {
          error: true,
          control: null,
        });
      },
      onRowSkipValidationAvailable(idx, abortFn) {
        const skipBtn = document.createElement('button');
        skipBtn.type = 'button';
        skipBtn.textContent = 'Skip verify';
        let used = false;
        skipBtn.addEventListener('click', () => {
          if (used) return;
          used = true;
          skipBtn.disabled = true;
          skipBtn.textContent = 'Skipping verify...';
          try { abortFn(); } catch {}
        });
        // Null label preserves the in-flight "Verifying X/Y words"
        // text set by onRowProgress.
        rows.add(rowId(idx), null, { control: skipBtn });
      },
      onRowDone(idx) {
        // Saved / failed paths already cleared the control via the
        // explicit control:null pass. This hook is a no-op now but
        // kept for caller-API compatibility with pipeline.js.
      },
      onConcurrencyChange(cap) {
        detailEl.textContent = `Worker cap reduced to ${cap} (browser couldn't spawn more).`;
      },
    },
    finish(saved, failed) {
      cancelBtn.removeEventListener('click', onCancelClick);
      cancelBtn.textContent = 'Done';
      cancelBtn.disabled = false;
      detailEl.textContent = `Done: ${saved} saved, ${failed} failed.`;
      // Stop the indeterminate animation; show a static full bar.
      barEl.value = 1;
      const onDone = () => finalClose();
      cancelBtn.addEventListener('click', onDone, { once: true });
    },
  };
}

