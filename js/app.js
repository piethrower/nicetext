// ---- ENCODE: source → cover ----
$('encode-go').addEventListener('click', async () => {
  // No pre-flight status string, the progress modal that opens
  // moments later carries the "in flight" signal. showPageStatus
  // only fires on terminal outcomes (success / cancel / error).
  clearPageStatus();
  try {
    const payload = await getSourceBytes();
    const sel = currentSelection;

    const seed = freshSeed();
    const styleLabel = sel.styleLabel;
    const modal = openProgressModal('Concealing secrets or silliness into the cover story');
    const totalBytes = payload.length;
    // Live-rate ETA: every sample uses performance.now() deltas from
    // THIS run on THIS machine, no hardcoded throughput, no cross-
    // machine assumptions. The tracker is fed on every progress event
    // (keeps EWMA smooth); modal.updateEta() throttles the DOM write
    // to ≥1 s. ETA renders on its own line in a slot whose layout box
    // is reserved via CSS so the modal doesn't shift when it appears.
    const etaTracker = createEtaTracker({ totalBytes });
    const onProgress = (info) => {
      // Setup phase fires before the engine starts (first-time dict /
      // model / grammar loads from /fixtures/). Bar holds
      // indeterminate; the engine path takes over once bits flow.
      if (info?.phase === 'setup') {
        modal.update(info.detail || 'Loading...', null);
        return;
      }
      const bitsRead = info?.bitsRead ?? 0;
      const bytesProcessed = Math.floor(bitsRead / 8);
      const fraction = totalBytes > 0
        ? Math.max(0, Math.min(1, bitsRead / (8 * totalBytes)))
        : null;
      modal.update(
        `Concealing your secret using:\n` +
        `${styleLabel}\n` +
        `${bytesProcessed.toLocaleString()} / ${totalBytes.toLocaleString()} bytes processed`,
        fraction,
      );
      modal.updateEta(etaTracker.update(bytesProcessed));
    };
    // Per-job skip-verification opt-out. AbortController is local to
    // this encode; firing it detaches the in-worker validator
    // (cancels its decode loop and removes the fan-out backpressure)
    // so the cover finishes at user-writer speed. Surfaced via the
    // modal's Skip Verification button on first validate-progress
    // event (so users only see the button when there IS a validator
    // running to skip).
    const skipValidationController = new AbortController();
    let skipBtnArmed = false;
    let validationSkipped = false;
    // The validator-pass decode reports wordsProcessed (no a-priori
    // total without scanning the cover up front). Bar stays
    // indeterminate, same as the standalone Reveal flow.
    const onValidateProgress = (info) => {
      // Once the user clicks Skip, ignore further validate-progress
      // events so the "skipped" status text isn't overwritten by
      // late-arriving "Verifying... N words checked" events while
      // the in-worker decode is still unwinding.
      if (validationSkipped) return;
      const wp = info?.wordsProcessed ?? 0;
      modal.updateValidate(
        `Verifying round-trip... ${wp.toLocaleString()} words checked`,
        null,
      );
      if (!skipBtnArmed) {
        skipBtnArmed = true;
        modal.enableSkipValidation(() => {
          validationSkipped = true;
          skipValidationController.abort();
        });
      }
    };
    let cover;
    let coverByteLen = 0;
    try {
      const inputStream = new ReadableStream({
        start(c) {
          if (payload.length > 0) c.enqueue(payload);
          c.close();
        },
      });
      // Cover-transforms rewriter + formatter blocks come from the
      // current selection's recipe (advanced mode) or from the
      // matched-card byos (chip / built-in mode). For premade cards
      // we look them up on the matched cards.data.js entry; for
      // advanced mode we read them off currentSelection.recipe.
      const coverTransforms = readCoverTransformsForCurrentSelection();
      const coverStream = await encodeJob({
        input: inputStream,
        dictPath: sel.dictPath,
        modelPath: sel.modelPath,
        grammarPath: sel.grammarPath,
        mode: sel.mode,
        randomSeed: seed,
        streamSeed: seed,
        signal: modal.signal,
        skipValidationSignal: skipValidationController.signal,
        onProgress,
        onValidateProgress,
        rewriter:    coverTransforms.rewriter,
        reformatter: coverTransforms.reformatter,
      });
      // Cover assembly. Two changes from the naive `acc += chunk`
      // pattern that was pegging Firefox at multi-MB cover sizes:
      //   1. Track UTF-8 byte count incrementally from each chunk's
      //      byteLength so we don't have to re-encode the whole
      //      cover later for the stats line (TextEncoder.encode on
      //      a 23M-char string costs ~2s on Firefox).
      //   2. Collect chunks into an array and Array.join at the end
      //      (O(N)) instead of `acc += chunk` (O(N²) due to JS
      //      string immutability, for 23M chars in ~1000 chunks
      //      that's ~500K chunk-sized copies of allocation churn).
      const dec = new TextDecoder();
      const reader = coverStream.getReader();
      const chunks = [];
      for (;;) {
        const { value, done } = await reader.read();
        if (done) break;
        coverByteLen += value.byteLength;
        chunks.push(dec.decode(value, { stream: true }));
      }
      chunks.push(dec.decode());
      // Flat (no model/grammar) was historically newline-terminated;
      // grammar/model-table covers already end with '\n'.
      if (!sel.modelPath && !sel.grammarPath) {
        chunks.push('\n');
        coverByteLen += 1;
      }
      cover = chunks.join('');
    } catch (e) {
      // Validation failure stays in the modal as a loud red banner so
      // it can't be missed; other errors close the modal and surface
      // through the status line as usual.
      if (/round-trip validation failed/.test(e?.message || '')) {
        modal.showError(
          'This attempt produced a broken cover and was discarded. ' +
          'Your previous good cover (if any) is still loaded; click ' +
          'Reveal to recover from it. Otherwise click Conceal to try ' +
          'again, or pick a different style.'
        );
      } else {
        modal.close();
      }
      throw e;
    }
    // Snap the bytes counter to total then transition to the
    // loading-into-display phase with a clear "done" framing.
    modal.update(
      `Concealing your secret using:\n` +
      `${styleLabel}\n` +
      `${totalBytes.toLocaleString()} / ${totalBytes.toLocaleString()} bytes processed`,
      1,
    );
    // Post-encode. setCoverText writes the full cover to the JS
    // source-of-truth (coverFullText) and mirrors only the first
    // COVER_PREVIEW_CHARS into the readonly textarea, so the sync
    // textarea DOM write is bounded regardless of total cover size.
    // coverByteLen was tracked incrementally in the reader loop, so
    // no second multi-MB UTF-8 re-encode is needed here.
    modal.update(
      `✓ Concealed, your cover story is ready.\n` +
      `Loading into the display (${cover.length.toLocaleString()} chars)...`,
      null,
    );
    await new Promise(r => requestAnimationFrame(r));
    setCoverText(cover);
    setCoverObservations({});
    coverSource = 'concealed';
    coverLoadedFromFile = '';
    coverStyleConsistent = true; // cover just produced by currentSelection
    lastConcealRatio = coverByteLen / Math.max(1, payload.length);
    updateCoverMeta(cover);
    updateCoverStats(cover, coverByteLen);
    refreshDecodeButton();
    refreshShareEnabled();
    await new Promise(r => requestAnimationFrame(r));
    modal.close();
    // source-stats already reflects bytes+sha256 via updateSourceStats;
    // encoding doesn't change the secret, so don't overwrite it.
    showPageStatus('success', 'Cover story', `concealed (${payload.length.toLocaleString()} bytes encoded)`, 'Click Conceal again for a different story with the same secret.');
    clearEncodeDirty();
    setActiveTab('cover');
  } catch (e) {
    if (!isCancelError(e)) console.error(e);
    if (isCancelError(e)) {
      showPageStatus('success', 'Cover story', 'conceal cancelled', 'Click Conceal again when ready.');
    } else {
      showPageStatus('error', 'Cover story', `conceal failed, ${friendlyError(e)}`, 'Pick a different style or shorten the secret.');
    }
  }
});

