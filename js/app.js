//              and CSS class (.advanced-body) keep their historical
//              names because the form predates the segmented
//              control; only the tab-level identifier and visible
//              label switched to Pro.
// The BYO card click flips to 'pro' (the entry point for full
// custom editing, preserving pre-three-tab UX). Mode persists in
// the nicetext-style-mode session cookie. Default = 'premade'.
const STYLE_MODE_COOKIE = 'nicetext-style-mode';
const STYLE_MODES = ['premade', 'custom', 'pro'];
function readStyleModeCookie() {
  for (const part of document.cookie.split(';')) {
    const [k, v] = part.trim().split('=');
    if (k === STYLE_MODE_COOKIE) {
      const val = decodeURIComponent(v || '');
      if (STYLE_MODES.includes(val)) return val;
    }
  }
  return 'premade';
}
function writeStyleModeCookie(m) {
  document.cookie = `${STYLE_MODE_COOKIE}=${encodeURIComponent(m)}; path=/; SameSite=Lax`;
}
function setStyleMode(mode) {
  if (!STYLE_MODES.includes(mode)) return;
  document.body.classList.toggle('style-mode-premade', mode === 'premade');
  document.body.classList.toggle('style-mode-custom',  mode === 'custom');
  document.body.classList.toggle('style-mode-pro',     mode === 'pro');
  for (const btn of document.querySelectorAll('.style-mode-toggle button[role="tab"]')) {
    btn.setAttribute('aria-selected', String(btn.dataset.styleMode === mode));
  }
  writeStyleModeCookie(mode);
  // Both Custom and Pro expose the same panel (Pro shows the whole
  // thing; Custom shows a CSS-scoped simplified subset). Only Premade
  // hides the panel entirely.
  if (mode === 'premade') closeAdvancedModal();
  else openAdvancedModal();
  updateStyleMeta();
  updateStyleStatsTitle();
}
