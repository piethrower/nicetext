// modal instead and only becomes "selected" after a successful Build.
function renderChips() {
  const container = $('style-chips');
  container.replaceChildren();
  for (const chip of CHIPS) {
    // The BYO entry (chip.kind === 'modal') is reachable via the
    // segmented Premade/Custom control, no card needed (it would
    // duplicate the toggle and clutter both modes).
    if (chip.kind === 'modal') continue;
    const btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'style-chip';
    btn.dataset.chipId = chip.id;
    const labelEl = document.createElement('span');
    labelEl.className = 'style-chip-label';
    labelEl.textContent = chip.label;
    const previewEl = document.createElement('span');
    previewEl.className = 'style-chip-preview';
    previewEl.textContent = chip.preview;
    btn.append(labelEl, previewEl);
    btn.addEventListener('click', () => { selectChip(chip.id); markEncodeDirty(); });
    container.appendChild(btn);
  }
}

