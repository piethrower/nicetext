function recordEveVerdict(knob, verdict, why, rule, contradiction, history) {
  if (!knob || !verdict) return;
  const existing = currentEveRatings.get(knob);
  const override = existing ? existing.override : null;
  // Carry forward ignored-rules set across re-records (a re-run
  // shouldn't blow away the developer's per-rule overrides).
  const ignoredRules = existing && existing.ignoredRules
    ? existing.ignoredRules
    : new Set();
  const entry = {
    auto: verdict,
    autoWhy: why || '',
    autoRule: rule || '',
    autoContradiction: !!contradiction,
    history: Array.isArray(history) ? history.slice() : [],
    ignoredRules,
    override,
  };
  applyEveOverrideState(entry);
  currentEveRatings.set(knob, entry);
}

// Recompute the effective verdict by re-running applyRule over the
// knob's history, skipping rules in ignoredRules. Result is written
// back into entry.effective + entry.effectiveRule. Annotation
// readers (ratingEmoji and the verdict-row renderer) consult these
// instead of entry.auto so a single source of truth drives both
// the eve-log row and the Story Style / Sources surfaces.
function applyEveOverrideState(entry) {
  if (!entry) return;
  if (entry.ignoredRules.size === 0) {
    entry.effective = entry.auto;
    entry.effectiveRule = entry.autoRule;
    entry.effectiveWhy = entry.autoWhy;
    entry.overridden = false;
    return;
  }
  const state = createVerdictState('eve-override-recompute');
  for (const h of entry.history) {
    if (entry.ignoredRules.has(h.rule)) continue;
    applyRule(state, {
      rule: h.rule, verdict: h.to,
      confidence: h.confidence, why: h.why,
    });
  }
  entry.effective = state.verdict;
  entry.effectiveRule = state.rule || '';
  entry.effectiveWhy = state.why || '';
  entry.overridden = true;
}

