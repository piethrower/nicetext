// ---- Source state machine ----
// `loadedBytes` (when set) is the bytes from a file upload or a binary
// decode. The textarea ALWAYS reflects either the user's text input or a
// preview of those bytes:
//   - bytes parse as UTF-8 → show the text, EDITABLE (user may tweak;
//     editing drops loadedBytes and the textarea becomes the truth).
//   - bytes are binary       → show "(binary: N bytes …)" placeholder,
//                              READONLY (a textarea can't sensibly edit
//                              binary; user must Clear or Download).
// Single source of truth for the source textarea content. Text bytes
// land as editable plain text; binary bytes land as a read-only
// od -ha hexdump. Metadata (source label, byte count, sha256) lives
// in source-stats, never in the textarea. Pure sync, large hexdumps
// for multi-MB binaries are wrapped by callers (loadSourceFromFileWithModal)
// that surround the write with rAF yields.
function renderSourceTextarea(bytes, ta) {
  if (!bytes || bytes.length === 0) {
    ta.readOnly = false;
    ta.classList.remove('is-binary');
    ta.wrap = 'soft';
    return;
  }
  if (bytesLooksLikeUtf8(bytes)) {
    ta.value = dec.decode(bytes);
    ta.readOnly = false;
    ta.classList.remove('is-binary');
    ta.wrap = 'soft';
  } else {
    ta.value = formatHexdump(bytes);
    ta.readOnly = true;
    ta.classList.add('is-binary');
    // Hexdump rows MUST NOT wrap: each row is "offset  hex-pairs  |ascii|"
    // and the alignment is unreadable if it breaks. wrap='off' surfaces
    // a horizontal scrollbar on narrow screens.
    ta.wrap = 'off';
  }
}

