// Modal-wrapped loadbox path. Always used when the user picks
// "Load into box" from the pipeline-consent dialog (i.e. file > the
// secret/cover threshold). Surfaces per-stage progress so multi-MB
// loads aren't a silent hang, yields rAF around the synchronous
// textarea write (the Safari pain point), and honours the modal's
// AbortSignal so Cancel returns early.
async function loadSourceFromFileWithModal(f) {
  const modal = openProgressModal('Loading Your Secret');
  try {
    modal.update(`Reading file (${f.size.toLocaleString()} bytes)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    const buf = await f.arrayBuffer();
    if (modal.signal.aborted) { try { modal.close(); } catch {} return; }

    modal.update(`Detecting encoding (${buf.byteLength.toLocaleString()} bytes)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    const bytes = new Uint8Array(buf);
    const isText = bytesLooksLikeUtf8(bytes);
    if (modal.signal.aborted) { try { modal.close(); } catch {} return; }

    loadedFromFile = f.name;
    loadedSource = 'file';
    loadedBytes = bytes;
    const ta = $('source-text');
    // Text path writes the decoded UTF-8 string; binary path writes a
    // hexdump (od -ha style). Either way the textarea holds actual
    // content: metadata (filename, byte count, sha256) lands in
    // source-stats via updateSourceStats below. Both paths can produce
    // multi-MB textarea writes; rAF yields keep the modal painted and
    // Cancel responsive across the synchronous DOM op.
    modal.update(
      isText
        ? `Decoding UTF-8 (${bytes.length.toLocaleString()} bytes)...`
        : `Formatting hexdump (${bytes.length.toLocaleString()} bytes)...`,
      null,
    );
    await new Promise(r => requestAnimationFrame(r));
    if (modal.signal.aborted) { try { modal.close(); } catch {} return; }

    modal.update(`Updating display...`, null);
    await new Promise(r => requestAnimationFrame(r));
    renderSourceTextarea(bytes, ta);
    await new Promise(r => requestAnimationFrame(r));

    updateSourceStats();
    markEncodeDirty();
    try { modal.close(); } catch {}
  } catch (err) {
    try { modal.showError(`Couldn't load file: ${err?.message || err}`); } catch {}
  }
}

