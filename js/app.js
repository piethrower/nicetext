// One DICTIONARIES entry per unique dict; alphabetical by label.
// Master leads via its '*' prefix sorting before A.
const DICTIONARIES = (() => {
  const seen = new Map();
  for (const card of cardsRegistry) {
    const id = dictIdForCard(card);
    if (seen.has(id)) continue;
    seen.set(id, { id, label: card.label || id, cardName: card.name });
  }
  return [...seen.values()].sort((a, b) => a.label.localeCompare(b.label));
})();

// Two STYLE_SOURCES entries per corpus-bound card (mt-rnd + mt-seq),
// keyed off story.style. Sequential cards (e.g. jfk-ordered) merge
// into the matching style's mt-seq slot via story.sentence. Plus the
// hand-written 'flat' and 'grammar:mit-names' specials.
const STYLE_SOURCES = (() => {
  const out = [];
  for (const card of cardsRegistry) {
    if (!card.story || RESERVED_STORY_STYLES.has(card.story.style)) continue;
    const styleKey = card.story.style;
    const sentence = card.story.sentence;
    const prefix = sentence === 'sequential' ? 'mt-seq' : 'mt-rnd';
    const id = `${prefix}:${styleKey}`;
    if (out.some(s => s.id === id)) continue;
    // Prefer card.label (work title, "Frankenstein", "Huckleberry
    // Finn") over card.casualLabel (author short, "Shelley", "Twain").
    // The work title is the name people recognize; the author is a
    // secondary handle. Swap-back would be a one-line tweak.
    const casual = card.label || card.casualLabel || styleKey;
    const verb = sentence === 'sequential' ? 'replay' : 'random pick from';
    out.push({
      id, kind: 'model-table', mode: sentence,
      label: `${casual} style: ${verb} ${casual} sentence patterns`,
      cardName: card.name, requireDict: styleKey,
    });
  }
  out.push(
    { id: 'flat', kind: 'flat',
      label: 'Flat: random words from dict (no sentence structure)' },
    { id: 'grammar:mit-names', kind: 'grammar',
      label: 'Names and Places (sample grammar)',
      path: 'grammars/mit-names.def', requireDict: 'mit' },
  );
  return out.sort((a, b) => a.label.localeCompare(b.label));
})();

