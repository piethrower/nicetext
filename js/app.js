// Sync state update from the textarea. Re-parses with the engine's
// canonical filter (parseTwlistLines, comma-normalized) and stores the
// entries + a fresh hash. No modal, no rewrite, that's the verify
// pass below. Called on every input event AND from the verify pass
// after rewriting the textarea, so the engine view of customTwlist is
// always in lockstep with the textarea content.
function commitCustomTwlist({ name, fromFile }) {
  const text = $('adv-twlist-text').value;
  if (!text.trim()) {
    customTwlist = null;
    refreshAdvancedValidation();
    return;
  }
  const { entries } = parseTwlistTextareaContent(text);
  const keepName = name ?? customTwlist?.name ?? '(typed)';
  const keepFromFile = fromFile ?? customTwlist?.fromFile ?? false;
  // Strip the lineIndex field, downstream consumers (worker pipeline,
  // share recipe) only know the {type, word} shape.
  const slim = entries.map(e => ({ type: e.type, word: e.word }));
  const built = { name: keepName, entries: slim, hash: '', fromFile: keepFromFile };
  customTwlist = built;
  refreshAdvancedValidation();
  // Returns the hash settle promise. Callers that need the hash before
  // proceeding (verify pass, anything that's about to read customTwlist.hash)
  // await it; fire-and-forget callers (the input listener, since typing
  // races itself) ignore the return value.
  return twlistHashPrefix(text).then(h => {
    if (customTwlist === built) {
      customTwlist.hash = h;
      // Re-render after the hash settles so the Build label flips
      // correctly when paste-back-same matches a previously-built hash.
      refreshAdvancedValidation();
    }
  });
}

// Verify pass: open progress modal, re-parse the textarea with
// reportRejections, rewrite the textarea so valid lines collapse to
// canonical "type,word" form and rejected lines get a "# " prefix
// (existing #-prefixed lines and blank rows are left untouched), then
// recommit state. scrollTop and selection are preserved across the
// write-back so the developer's eye lands where it was.
//
// Triggered from paste, file load, and blur-after-edit. Typing alone
// does NOT trigger this, that would pop a modal on every keystroke.
async function verifyAndCommentCustomTwlist({ name, fromFile, text } = {}) {
  const ta = $('adv-twlist-text');
  // text != null means file-load / paste path: caller passed raw
  // content and the textarea hasn't seen it yet. text == null means
  // blur path: read what the user typed.
  const rawIn = text != null ? text : ta.value;
  if (!rawIn.trim()) {
    twlistDirty = false;
    customTwlist = null;
    refreshAdvancedValidation();
    return;
  }
  const modal = openProgressModal('Validating Custom Word List');
  modal.update(`Preparing (${rawIn.length.toLocaleString()} chars)...`, null);
  // Yield one frame so the modal actually paints before the
  // synchronous parse hogs the main thread on a large list.
  await new Promise(r => requestAnimationFrame(r));

  // File-load / paste path: write the raw content to the textarea
  // now, with an rAF yield around the synchronous DOM op so the
  // modal stays painted (Safari blocks for multi-MB strings).
  if (text != null) {
    modal.update(`Loading textarea (${rawIn.length.toLocaleString()} chars)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    if (modal.signal.aborted) { try { modal.close(); } catch {} return; }
    ta.value = rawIn;
    await new Promise(r => requestAnimationFrame(r));
  }

  if (modal.signal.aborted) { try { modal.close(); } catch {} return; }
  const before = ta.value;
  modal.update(`Splitting lines (${before.length.toLocaleString()} chars)...`, null);
  await new Promise(r => requestAnimationFrame(r));
  const lines = before.split('\n');

  if (modal.signal.aborted) { try { modal.close(); } catch {} return; }
  modal.update(`Parsing ${lines.length.toLocaleString()} lines...`, null);
  await new Promise(r => requestAnimationFrame(r));
  const { entries, rejections } = parseTwlistTextareaContent(before);
  const entryByIdx = new Map(entries.map(e => [e.lineIndex, e]));
  const rejByIdx = new Set(rejections.map(r => r.lineIndex));

  if (modal.signal.aborted) { try { modal.close(); } catch {} return; }
  modal.update(
    `Rewriting (${entries.length.toLocaleString()} valid, ${rejections.length.toLocaleString()} rejected)...`,
    null,
  );
  await new Promise(r => requestAnimationFrame(r));
  const rewritten = lines.map((line, i) => {
    const entry = entryByIdx.get(i);
    if (entry) return `${entry.type},${entry.word}`;
    if (rejByIdx.has(i)) return `# ${line}`;
    return line;
  });
  const after = rewritten.join('\n');

  if (modal.signal.aborted) { try { modal.close(); } catch {} return; }
  if (after !== before) {
    modal.update(`Updating display (${after.length.toLocaleString()} chars)...`, null);
    await new Promise(r => requestAnimationFrame(r));
    const scrollTop = ta.scrollTop;
    const selStart = ta.selectionStart;
    const selEnd = ta.selectionEnd;
    ta.value = after;
    ta.scrollTop = scrollTop;
    try { ta.setSelectionRange(selStart, selEnd); } catch {}
    await new Promise(r => requestAnimationFrame(r));
  }

  modal.update(`Computing word-list hash...`, null);
  await new Promise(r => requestAnimationFrame(r));
  twlistDirty = false;
  await commitCustomTwlist({ name, fromFile });

  // Auto-dismiss when there's nothing to tell the developer about.
  // If every line passed validation, the modal is just noise, the
  // textarea was either already canonical or got silently normalized.
  // Only surface the modal when at least one line was rejected so
  // the developer knows what to fix.
  if (rejections.length === 0) {
    modal.close();
    return;
  }
  const n = rejections.length;
  const byReason = { malformed: 0, 'lexer-rejected': 0, redacted: 0 };
  for (const r of rejections) byReason[r.reason] = (byReason[r.reason] || 0) + 1;
  const reasonParts = [];
  if (byReason.malformed)        reasonParts.push(`${byReason.malformed} malformed`);
  if (byReason['lexer-rejected']) reasonParts.push(`${byReason['lexer-rejected']} lexer-rejected`);
  if (byReason.redacted)         reasonParts.push(`${byReason.redacted} matched the redacted word/phrase list`);
  const summary = `${n.toLocaleString()} ${n === 1 ? 'line' : 'lines'} commented out (${reasonParts.join('; ')}).`;
  modal.update(summary, 1);
  modal.done();
}

