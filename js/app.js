//   'premade': slot 4 shows the full chip grid (BYO card included).
//   'custom':  slot 4 hides every chip except the BYO one, and a
//              CSS-scoped simplified subset of the BYOS panel
//              (#advanced-body) is shown below it (sentence model,
//              emoji style pillbox, word swap row, flourishes row).
//   'pro':     same chip behavior as 'custom' but the BYOS panel is
//              shown in full (every section + every row). Pro is
