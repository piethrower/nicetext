function bytesEqual(a, b) {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false;
  return true;
}

// runReveal: the full decode flow. Called from the in-modal Reveal
// button (#reveal-modal-go). The pillbox (#decode-go) itself only
// opens #reveal-modal as a staging surface.
async function runReveal() {
  clearPageStatus();
  try {
    const cover = await getCoverText();
    if (!cover.trim()) {
      showPageStatus('error', 'Cover story', 'nothing to reveal', 'Load or paste a cover story in The Cover Story first.');
      return;
    }

    const sel = currentSelection;
    // Match the Conceal modal's 3-line layout: header / style /
    // counter: each on its own line so longer style names don't
    // wrap and shift surrounding elements. styleLabel is the same
    // descriptor Conceal shows.
    const styleLabel = sel.styleLabel;
    const modal = openProgressModal('Revealing secrets or silliness from the cover story');
    // The decode engine emits wordsProcessed only; total isn't known
    // without scanning the cover up front. Bar stays indeterminate.
    const onProgress = (info) => {
      if (info?.phase === 'setup') {
        modal.update(info.detail || 'Loading...', null);
        return;
      }
      const wp = info?.wordsProcessed ?? 0;
      const be = info?.bytesEmitted ?? 0;
      modal.update(
        `Revealing your secret using:\n` +
        `${styleLabel}\n` +
        `\n` +
        `${wp.toLocaleString()} words checked\n` +
        `${be.toLocaleString()} bytes revealed`,
        null,
      );
    };
    let recovered;
    try {
      const enc = new TextEncoder();
      const inputStream = new ReadableStream({
        start(c) {
          c.enqueue(enc.encode(cover));
          c.close();
        },
      });
      const recoveredStream = await decodeJob({
        input: inputStream,
        dictPath: sel.dictPath,
        signal: modal.signal,
        onProgress,
      });
      const chunks = [];
      let total = 0;
      const reader = recoveredStream.getReader();
      for (;;) {
        const { value, done } = await reader.read();
        if (done) break;
        chunks.push(value);
        total += value.length;
      }
      recovered = new Uint8Array(total);
      let offset = 0;
      for (const c of chunks) { recovered.set(c, offset); offset += c.length; }
    } catch (e) {
      modal.close();
      throw e;
    }
    modal.close();
    const currentSource = await getSourceBytes();
    const sourceIsEmpty = currentSource.length === 0;

    if (!sourceIsEmpty && bytesEqual(currentSource, recovered)) {
      showPageStatus('success', 'Secret', `already matches your current secret (${recovered.length.toLocaleString()} bytes)`, 'Nothing to overwrite.');
      setActiveTab('secret');
      return;
    }
    if (!sourceIsEmpty) {
      const replace = await showOverwriteConfirm();
      if (!replace) {
        showPageStatus('success', 'Secret', 'kept your current secret', 'Click Reveal again to overwrite it.');
        setActiveTab('secret');
        return;
      }
    }
    setSourceFromDecodedBytes(recovered);
    // The cover decoded cleanly under currentSelection, that's the
    // strongest proof we have that this style matches this cover.
    // Mark consistent so Share is enabled.
    coverStyleConsistent = true;
    refreshShareEnabled();
    showPageStatus('success', 'Secret', `revealed (${recovered.length.toLocaleString()} bytes)`, 'Click Save to keep it as a file.');
    setActiveTab('secret');
  } catch (e) {
    if (!isCancelError(e)) console.error(e);
    if (isCancelError(e)) {
      showPageStatus('success', 'Secret', 'reveal cancelled', 'Click Reveal again when ready.');
    } else {
      showPageStatus('error', 'Secret', `reveal failed, ${friendlyError(e)}`, 'Check the cover story is from the same style.');
    }
  }
}

