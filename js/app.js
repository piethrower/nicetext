// ---- Random bytes generator ----
// Uses crypto.getRandomValues via the format-aware helper in
// js/src/random-secret-formats.js. The "intended" source per the 1997
// motivation: recovered bytes from any cover should be statistically
// indistinguishable from a random number generator OR the output of a
// real cipher. The format dropdown shapes the output to look like a
// specific tool's ciphertext (OpenSSL enc, VeraCrypt) so an interceptor
// who recovers the secret can't trivially distinguish it from genuine
// ciphertext from that tool.
//
// No size cap. A known cap would let an interceptor rule out
// nicetext-generated random bytes above that size, undermining the
// stego cover story. For requests that exceed the pipeline secret
// threshold (T_SECRET_BYTES in pipeline.js), the same load-or-pipeline
// consent dialog used for file ingress is reused: the in-memory bytes
// are wrapped as a synthetic File and handed to runPipeline.
const RAND_CHUNK = 65536;
function fillRandomChunked(buf) {
  // crypto.getRandomValues is capped at 65536 bytes per call.
  for (let off = 0; off < buf.length; off += RAND_CHUNK) {
    crypto.getRandomValues(buf.subarray(off, Math.min(off + RAND_CHUNK, buf.length)));
  }
}
function randomSecretFilename(fmt) {
  if (fmt === FORMAT_OPENSSL) return 'random.openssl.enc';
  if (fmt === FORMAT_VERACRYPT) return 'random.veracrypt.hc';
  return 'random.bin';
}
function updateRandomBytesHint() {
  const hint = $('random-bytes-hint');
  if (!hint) return;
  const n = parseInt($('random-bytes-count').value, 10);
  const fmt = $('random-bytes-format').value;
  if (!Number.isFinite(n) || n < 1) { hint.textContent = ''; return; }
  if (fmt === FORMAT_PURE) { hint.textContent = ''; return; }
  const total = plannedTotalBytes(fmt, n);
  if (fmt === FORMAT_OPENSSL) {
    const s = total === n
      ? `Output: ${total.toLocaleString()} bytes (8-byte "Salted__" + 8-byte salt + AES-CBC body, 16-byte aligned).`
      : `Output: ${total.toLocaleString()} bytes (rounded from ${n.toLocaleString()}; AES-CBC body must be 16-byte aligned, body min 16 bytes).`;
    hint.textContent = s;
  } else if (fmt === FORMAT_VERACRYPT) {
    const minKB = (VERACRYPT_MIN_BYTES / 1024).toLocaleString();
    if (total === n) {
      hint.textContent = `Output: ${total.toLocaleString()} bytes (sector-aligned VeraCrypt container).`;
    } else {
      hint.textContent = `Output: ${total.toLocaleString()} bytes. VeraCrypt requires ≥${minKB} KB; will bump from ${n.toLocaleString()}.`;
    }
  }
}
