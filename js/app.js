// Convenience layer: count slur/phrase hits in the given corpus text
// using the same getRedactedMatcher engine that genmodel/listword use
// at model-build time. Reports the count as a status-line suffix so
// the user can see ahead of build what will be redacted.
//
// Defense-in-depth applies regardless: engine's phraseFuse during
// model build redacts everything whether or not the count is shown.
function redactionCountForText(text) {
  if (!_redactedMatcher || !text) return '';
  const re = buildLocalRedactionScanRegex(_redactedMatcher);
  if (!re) return '';
  let single = 0, phrase = 0;
  for (const m of text.matchAll(re)) {
    if (/\s/.test(m[0])) phrase++; else single++;
  }
  const total = single + phrase;
  if (total === 0) return '';
  const parts = [];
  if (single) parts.push(`${single} word${single === 1 ? '' : 's'}`);
  if (phrase) parts.push(`${phrase} phrase${phrase === 1 ? '' : 's'}`);
  return ` · ${parts.join(', ')} will be redacted during build`;
}

// Build a case-insensitive whole-word RegExp from the matcher's
// singles + phrase head entries for UI-side scan-and-count. Greedy
// longest-first so multi-word entries win. Built once per matcher
// load and cached.
let _redactionScanRegex; // undefined = not built; null = empty; RegExp otherwise
function buildLocalRedactionScanRegex(matcher) {
  if (_redactionScanRegex !== undefined) return _redactionScanRegex;
  const phrases = [];
  for (const arr of matcher.phraseIndex.values()) {
    for (const cand of arr) phrases.push(cand.parts.join(' '));
  }
  phrases.sort((a, b) => b.length - a.length);
  const escape = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const alts = phrases.map(escape);
  if (alts.length === 0) { _redactionScanRegex = null; return null; }
  _redactionScanRegex = new RegExp('\\b(?:' + alts.join('|') + ')\\b', 'gi');
  return _redactionScanRegex;
}

