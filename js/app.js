// SHA-256 via Web Crypto. Truncated display (first 16 hex chars) inline,
// full hash in title attribute (hover to see). Returns a promise.
let lastStatsRequestId = 0;
async function sha256Hex(bytes) {
  const buf = await crypto.subtle.digest('SHA-256', bytes);
  const arr = new Uint8Array(buf);
  let s = '';
  for (const b of arr) s += b.toString(16).padStart(2, '0');
  return s;
}
// Ratio label for "story is N× bigger than the secret". >100 → no
// decimal; >10 → 1 decimal; ≤10 → 2 decimals. (Always rendered with
// thousands separators on the integer portion.)
function formatRatio(r) {
  if (!Number.isFinite(r) || r <= 0) return '0';
  if (r > 100) return Math.round(r).toLocaleString();
  if (r > 10)  return r.toFixed(1);
  return r.toFixed(2);
}

