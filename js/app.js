function effectiveFilename() { return wrapState.filename || 'message'; }
function effectiveSubject() { return wrapState.subject || 'Re: Your request'; }

function wrapOutputName() {
  let name = `${effectiveFilename()}.${WRAP_ENVELOPE_EXT[wrapState.envelope]}`;
  for (const layer of wrapState.stack) name = `${name}.${WRAP_FORMAT_EXT[layer]}`;
  return name;
}

function wrapOutputMime() {
  if (wrapState.stack.length > 0) {
    return WRAP_FORMAT_MIME[wrapState.stack[wrapState.stack.length - 1]];
  }
  return WRAP_ENVELOPE_MIME[wrapState.envelope];
}

function wrapOutputIsBinary() {
  if (wrapState.stack.length > 0) {
    return wrapState.stack.includes('gzip');
  }
  return wrapState.envelope === 'pdf';
}

// Build the layer stack passed to applyStack. Each layer carries a
// filename that accumulates extensions from the layers below it (so
// gzip FNAME, PEM BEGIN, uuencode begin line all reflect what the
// file's name is at that point in the wrapping). The envelope (if any)
// is the innermost layer; format layers wrap outward.
function wrapBuildLayers() {
  const base = effectiveFilename();
  const subject = effectiveSubject();
  const layers = [];
  let currentName = base;
  if (wrapState.envelope !== 'none') {
    layers.push({ type: wrapState.envelope, filename: currentName, subject });
    currentName = `${currentName}.${WRAP_ENVELOPE_EXT[wrapState.envelope]}`;
  }
  for (const t of wrapState.stack) {
    layers.push({ type: t, filename: currentName, subject });
    currentName = `${currentName}.${WRAP_FORMAT_EXT[t]}`;
  }
  return layers;
}

// Run cover text through escape + applyStack, return the wrapped bytes.
async function wrapCoverToBytes(coverText) {
  const source = new ReadableStream({
    start(c) {
      const b = new TextEncoder().encode(coverText);
      if (b.length) c.enqueue(b);
      c.close();
    },
  });
  const escaped = source.pipeThrough(coverEscapeTransform());
  const wrapped = coverApplyStack(escaped, wrapBuildLayers());
  const reader = wrapped.getReader();
  const chunks = [];
  let total = 0;
  for (;;) {
    const { value, done } = await reader.read();
    if (done) break;
    chunks.push(value);
    total += value.length;
  }
  const out = new Uint8Array(total);
  let off = 0;
  for (const c of chunks) { out.set(c, off); off += c.length; }
  return out;
}

// Inverse: run wrapped bytes through autoStrip, return the bare cover
// text. Used by cover-paste and cover-file ingress.
async function autoStripCoverBytes(bytes) {
  const source = new ReadableStream({
    start(c) {
      if (bytes.length) c.enqueue(bytes);
      c.close();
    },
  });
  const bare = await coverAutoStrip(source);
  const reader = bare.getReader();
  const chunks = [];
  let total = 0;
  for (;;) {
    const { value, done } = await reader.read();
    if (done) break;
    chunks.push(value);
    total += value.length;
  }
  const out = new Uint8Array(total);
  let off = 0;
  for (const c of chunks) { out.set(c, off); off += c.length; }
  return new TextDecoder().decode(out);
}

async function autoStripCoverText(text) {
  return autoStripCoverBytes(new TextEncoder().encode(text));
}

// Layer-picker UX (audit 2026-05-18). Detects the wrapper chain on the
// raw input bytes; if one or more layers were found, opens the cover-
// layer-picker dialog and lets the user choose which layers to peel.
// All layers checked by default. Returns:
//   { text, layers, chosen, fellBack } : user clicked Unwrap.
//   null                               : user clicked Cancel.
// On bare input (zero layers detected), no modal is shown and the
// returned `text` is the raw input as UTF-8.
async function unwrapCoverWithPicker(bytes) {
  const sourceStream = new ReadableStream({
    start(c) { if (bytes.length) c.enqueue(bytes); c.close(); },
  });
  const { layers } = await coverDetectLayers(sourceStream);
  if (layers.length === 0) {
    return { text: new TextDecoder().decode(bytes), layers: [], chosen: [], fellBack: false };
  }
  const chosen = await openCoverLayerPicker(layers);
  if (chosen === null) return null;
  const result = await coverStripWithFallback(bytes, chosen);
  return {
    text: new TextDecoder().decode(result.bytes),
    layers,
    chosen,
    fellBack: result.fellBack,
  };
}

// Modal that lists the detected wrapper chain (outer → inner) with one
// checkbox per layer, all checked by default. Cascade semantics: the
// chain is logically nested, to reach layer K's payload, layers 1..K-1
// must be stripped too. So unchecking row K cascades to uncheck
// K+1..N; checking row K cascades to check 1..K. The user's real
// choice is "how deep to unwrap" (0..N from the outside in), kept
// visible as a familiar checkbox list. Resolves with the ordered
// prefix [layers[0..depth-1]] the user kept checked, or null on Cancel.
const _coverLayerPickerModal = createOptionsModal({
  dialog: 'cover-layer-picker',
  closeX: 'cover-layer-picker-close',
  buttons: [
    { id: 'cover-layer-picker-cancel', value: 'cancel' },
    { id: 'cover-layer-picker-ok', value: 'ok', primary: true },
  ],
});
async function openCoverLayerPicker(layers) {
  const list = $('cover-layer-picker-list');
  const fallbackEl = $('cover-layer-picker-fallback');
  const checkboxes = [];
  const onCheckboxChange = (ev) => {
    const idx = Number(ev.target.dataset.layerIndex);
    if (ev.target.checked) {
      for (let i = 0; i < idx; i++) checkboxes[i].checked = true;
    } else {
      for (let i = idx + 1; i < checkboxes.length; i++) checkboxes[i].checked = false;
    }
  };
  const choice = await _coverLayerPickerModal.open({
    populate: () => {
      list.replaceChildren();
      if (fallbackEl) fallbackEl.hidden = true;
      checkboxes.length = 0;
      for (let i = 0; i < layers.length; i++) {
        const li = document.createElement('li');
        li.className = 'layer-picker-row';
        const id = `layer-pick-${i}`;
        const cb = document.createElement('input');
        cb.type = 'checkbox';
        cb.id = id;
        cb.checked = true;
        cb.dataset.layerIndex = String(i);
        const label = document.createElement('label');
        label.htmlFor = id;
        label.textContent = `${i + 1}. ${layers[i]}`;
        li.append(cb, label);
        list.appendChild(li);
        checkboxes.push(cb);
        cb.addEventListener('change', onCheckboxChange);
      }
    },
    cleanup: () => {
      for (const cb of checkboxes) cb.removeEventListener('change', onCheckboxChange);
    },
  });
  if (choice !== 'ok') return null;
  let depth = 0;
  for (let i = 0; i < checkboxes.length; i++) {
    if (checkboxes[i].checked) depth = i + 1;
    else break;
  }
  return layers.slice(0, depth);
}

// Cumulative filename at a given wrap layer. layerIndex = -1 → after
// the envelope (no stack ext); layerIndex = i ≥ 0 → after applying
// stack[0..i].
function filenameAtLayer(layerIndex) {
  let name = `${effectiveFilename()}.${WRAP_ENVELOPE_EXT[wrapState.envelope]}`;
  for (let i = 0; i <= layerIndex; i++) name = `${name}.${WRAP_FORMAT_EXT[wrapState.stack[i]]}`;
  return name;
}

function renderWrapStack() {
  const list = $('share-cover-stack-list');
  list.replaceChildren();
  const N = wrapState.stack.length;
  // Render stack in reverse: outermost layer (stack[N-1]) on top, so
  // the recipient's first unwrap step is at the visual top.
  for (let visualPos = 0; visualPos < N; visualPos++) {
    const i = N - 1 - visualPos;
    const step = visualPos + 1;
    const layer = wrapState.stack[i];
    const li = document.createElement('li');
    li.className = 'share-wrap-stack-row';

    const controls = document.createElement('div');
    controls.className = 'share-wrap-stack-controls';
    const sel = document.createElement('select');
    sel.className = 'share-wrap-stack-type';
    for (const t of ['base64', 'gzip', 'uuencode']) {
      const opt = document.createElement('option');
      opt.value = t;
      opt.textContent = t;
      if (t === layer) opt.selected = true;
      sel.appendChild(opt);
    }
    sel.addEventListener('change', () => {
      wrapState.stack[i] = sel.value;
      renderWrapStack();
      renderWrapOutput();
    });
    const removeBtn = document.createElement('button');
    removeBtn.type = 'button';
    removeBtn.className = 'share-wrap-stack-remove';
    removeBtn.textContent = '×';
    removeBtn.title = 'Remove this layer';
    removeBtn.addEventListener('click', () => {
      wrapState.stack.splice(i, 1);
      renderWrapStack();
      renderWrapOutput();
    });
    controls.appendChild(sel);
    controls.appendChild(removeBtn);

    const hint = document.createElement('p');
    hint.className = 'share-wrap-hint';
    const template = WRAP_FORMAT_HINT[layer] || '';
    const cmd = template
      .replace('{filename}', filenameAtLayer(i))
      .replace('{parent}', filenameAtLayer(i - 1));
    hint.textContent = `${step}. Open with: ${cmd}`;

    li.appendChild(controls);
    li.appendChild(hint);
    list.appendChild(li);
  }
  $('share-cover-stack-add').disabled = N >= WRAP_STACK_CAP;
}

// Broadcast hook, fires after every wrapState mutation so an
// embedding surface (today: #pipeline-consent) can re-render its
// derived UI live (per-file filename previews).
let onWrapStateChanged = null;
function renderWrapOutput() {
  // Filename label carries the active suffix in parens, e.g.
  // "File name (.eml.gz):" so the developer can see the
  // disguise + stack chain without a separate output-name line.
  const suffix = wrapOutputName().slice(effectiveFilename().length);
  const suffixEl = $('share-cover-filename-suffix');
  if (suffixEl) suffixEl.textContent = suffix;
  renderWrapHint();
  renderWrapPuzzle();
  if (onWrapStateChanged) {
    try { onWrapStateChanged(); } catch {}
  }
}

function renderWrapHint() {
  const env = wrapState.envelope;
  const template = WRAP_ENVELOPE_HINT[env] || '';
  const step = wrapState.stack.length + 1;
  if (!template) { $('share-cover-envelope-hint').textContent = ''; return; }
  // Substitute {filename} with the envelope-level filename, by this
  // step every stack layer has been peeled off.
  const hint = template.includes('{filename}')
    ? template.replace('{filename}', filenameAtLayer(-1))
    : template;
  $('share-cover-envelope-hint').textContent = `${step}. Open with: ${hint}`;
}

function renderWrapPuzzle() {
  const el = $('share-cover-puzzle');
  const hasWrap = wrapState.stack.length > 0 || wrapState.envelope !== 'none';
  if (!hasWrap) { el.textContent = ''; return; }
  const steps = wrapState.stack.length + 1;
  const range = steps === 1 ? 'step 1' : `steps 1–${steps}`;
  el.textContent =
    `Follow the ${range} above as a fun puzzle to see the cover story. ` +
    `Or, just load ${wrapOutputName()} into NiceText and it'll figure it out for you.`;
}

