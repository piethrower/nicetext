function refreshCustomTwlistUI() {
  const isCustomTw = $('adv-src-customtw').checked;
  $('adv-twlist-row').hidden = !isCustomTw;
  if (!isCustomTw) collapseInfoDisclosure('adv-twlist-info-toggle', 'adv-twlist-info-body');
  const status = $('adv-twlist-status');
  const typesCell = $('adv-src-customtw-types');
  const wordsCell = $('adv-src-customtw-words');
  if (customTwlist) {
    const origin = customTwlist.fromFile ? `loaded from ${customTwlist.name}` : 'typed';
    status.textContent = `${customTwlist.entries.length.toLocaleString()} entries · ${origin}`;
    status.classList.add('has-file');
    const types = new Set(), words = new Set();
    for (const e of customTwlist.entries) { types.add(e.type); words.add(e.word); }
    if (typesCell) typesCell.textContent = types.size.toLocaleString();
    if (wordsCell) wordsCell.textContent = words.size.toLocaleString();
  } else {
    status.textContent = 'No word-list loaded.';
    status.classList.remove('has-file');
    if (typesCell) typesCell.textContent = '—';
    if (wordsCell) wordsCell.textContent = '—';
  }
}

