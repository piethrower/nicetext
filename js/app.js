// Dispatch the Eavesdropper analysis to js/eve-worker.js so the
// main thread stays responsive on multi-MB covers and large
// fixture loads. The worker emits a stream of events; this
// renderer maps them into log rows.
async function runEveAnalysis() {
  eveRunInFlight = true;
  eveCancelRequested = false;
  $('eve-go').hidden = true;
  $('eve-cancel').hidden = false;
  eveLogClear();
  eveModal.open('Eve is running');
  eveModal.update({ test: 'starting...', detail: '' });
  // Per-run cancel handler: route the modal's cancel pathway (top
  // X, bottom Cancel, Escape, or the in-tab #eve-cancel button) to
  // the live worker so the analysis loop sees the abort. The modal
  // already aborts its own AbortSignal; this just gets the message
  // through to the worker side.
  eveModal.onCancel(() => {
    eveCancelRequested = true;
    if (eveActiveWorker) {
      try { eveActiveWorker.postMessage({ type: 'cancel' }); } catch {}
    }
    eveModal.update({
      test: 'cancelling...',
      detail: 'waiting for worker to stop the current step',
    });
  });

  // Slice the cover per the dropdown. "all" passes the full
  // cover; a preset key picks a byte budget and we slice at
  // the next sentence boundary.
  const sliceKey = $('eve-slice-size').value || 'all';
  let coverSlice = coverFullText;
  if (sliceKey !== 'all') {
    const preset = EVE_SLICE_PRESETS.find(p => p.key === sliceKey);
    if (preset) {
      coverSlice = sliceCoverAtSentenceBoundary(coverFullText, preset.bytes);
      appendEveSectionBanner(
        `Slice: first ${coverSlice.length.toLocaleString()} chars of ${coverFullText.length.toLocaleString()} ` +
        `(rounded up to next sentence boundary; preset ${preset.label})`
      );
    }
  }

  const worker = await createEveWorker(new URL('./eve-worker.js', import.meta.url));
  // Wire main-thread resource-loader proxy: any loadResource call
  // inside eve-worker.js routes its request up to main here, hits
  // the shared cache (or loads via the resource-worker pool), and
  // sends the SAB back through structured clone.
  const detachLoaderProxy = attachLoaderProxy(worker);
  eveActiveWorker = worker;

  try {
    await new Promise((resolve, reject) => {
      worker.onmessage = ({ data }) => {
        if (!data || data.type !== 'event') return;
        const ev = data.event;
        switch (ev.kind) {
          case 'banner':
            appendEveSectionBanner(ev.text);
            break;
          case 'progress':
            // Single-line progress row that updates in place per
            // distinct test, so the log doesn't fill with 1000s of
            // intermediate progress messages. Also mirrored into
            // the fixed top banner.
            updateEveProgressRow(ev);
            {
              const totalText = (typeof ev.count === 'number' && ev.total)
                ? `  (${ev.count} / ${ev.total})`
                : (typeof ev.count === 'number' ? `  (${ev.count})` : '');
              eveModal.update({
                test: ev.test,
                detail: `${ev.what || ''}${totalText}`,
              });
            }
            break;
          case 'verdict':
            // Record into the ratings side-channel BEFORE rendering
            // so the renderer reads back the (just-stored) history
            // and can build the per-rule ignore/restore controls.
            if (ev.knob && ev.verdict) {
              recordEveVerdict(ev.knob, ev.verdict, ev.why, ev.rule, ev.contradiction, ev.history);
            }
            appendEveLog({
              kind: ev.verdict ? 'verdict-row' : 'test',
              title: ev.title,
              call: ev.call,
              detail: ev.detail,
              verdict: ev.verdict,
              why: ev.why,
              rule: ev.rule,
              contradiction: ev.contradiction,
              knob: ev.knob,
            });
            break;
          case 'load-start':
            eveModal.rows.add(ev.id, 'loading');
            break;
          case 'load-progress':
            eveModal.rows.update(ev.id, ev.label || '');
            break;
          case 'load-end':
            eveModal.rows.remove(ev.id);
            break;
          case 'pool-init':
            // No pre-populate: rows materialize on worker-busy and
            // disappear on worker-idle, so the modal stays compact.
            break;
          case 'worker-busy':
            eveModal.rows.add(`w${ev.workerId}`, ev.label || ev.jobKind || '');
            break;
          case 'worker-progress':
            // Mid-job progress label from a job handler. Updates the
            // row text in place so big TW-lists / corpus precomputes
            // show parse advancing.
            eveModal.rows.update(`w${ev.workerId}`, ev.label || ev.jobKind || '');
            break;
          case 'worker-idle':
            eveModal.rows.remove(`w${ev.workerId}`);
            break;
          case 'detail':
            appendEveLog({ kind: 'detail', detail: ev.text });
            break;
          case 'stats':
            appendEveLog({
              kind: 'detail',
              detail:
                `Combinations alive: ${ev.combinationsAlive.toLocaleString()} ` +
                `(${ev.stylesConsidered} of ${ev.stylesIn} styles considered, ` +
                `${ev.augCount} augment tuples per non-flat style)`,
            });
            break;
          case 'done':
            applyEveRatings();
            resolve();
            break;
          case 'cancelled':
            applyEveRatings();
            appendEveSectionBanner('Analysis cancelled.');
            resolve();
            break;
          case 'error': {
            // The orchestrator's 'error' message is the upstream
            // err.stack (which now carries the worker-side stack
            // appended in pool.js). Set the rejection's .stack to
            // that string so appendEveLog's "err.stack ||
            // err.message" path surfaces the full chain, including
            // the recursive line inside the worker.
            const e = new Error(ev.message);
            if (ev.message) e.stack = ev.message;
            reject(e);
            break;
          }
        }
      };
      worker.onerror = (err) => reject(err);
      worker.postMessage({ type: 'run', suspectedText: coverSlice, observations: coverObservations });
    });
  } finally {
    try { detachLoaderProxy(); } catch {}
    try { await worker.terminate(); } catch {}
    eveActiveWorker = null;
    eveCurrentProgressTest = null;
    eveProgressRow = null;
    eveRunInFlight = false;
    $('eve-go').hidden = false;
    $('eve-cancel').hidden = true;
    eveModal.close();
  }
}

