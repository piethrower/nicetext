    rewriterTyposEnabled:     $('adv-rewriter-typos-enabled').checked,
    rewriterTyposIntensity:   parseInt($('adv-rewriter-typos-intensity').value,   10) || 100,
    rewriterTyposMode:        $('adv-rewriter-typos-mode').value,
    rewriterVoiceEnabled:     $('adv-rewriter-voice-enabled').checked,
    rewriterVoiceIntensity:   parseInt($('adv-rewriter-voice-intensity').value,   10) || 100,
    rewriterVoiceMode:        $('adv-rewriter-voice-mode').value,
    rewriterXanaxEnabled:     $('adv-rewriter-xanax-enabled').checked,
    rewriterXanaxIntensity:   parseInt($('adv-rewriter-xanax-intensity').value,   10) || 100,
    reformatterLineBreakEnabled:     $('adv-formatter-linebreak-enabled').checked,
    reformatterLineBreakIntensity:   parseInt($('adv-formatter-linebreak-intensity').value, 10) || 100,
    reformatterLineBreakMode:        $('adv-formatter-linebreak-mode').value,
    reformatterCaseEnabled:          $('adv-formatter-case-enabled').checked,
    reformatterCaseIntensity:        parseInt($('adv-formatter-case-intensity').value, 10) || 100,
