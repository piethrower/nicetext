// ---- Share-style modal ----
// Open: build the URL + English instructions for the active selection
// and pre-fill the modal. Close: standard dialog.close() flows. The
// "Share" button (Web Share API) and the "Send via mail…" button are
// mutually exclusive: when navigator.share is available we surface
// only Share; otherwise we surface only the mailto fallback.
// Produce the share URL for the current recipe. Always emits
// `${baseHref}#${getBYOSID(byos, cards)}` -- nickname-with-rev when a
// card matches, long-form byosID otherwise. There is no second URL
// format: the byosID is the byosID, period. The fragment round-trips
// through observeURLFragment on the recipient's page.
function shareURL(baseHref, recipe, cards) {
  let byos = null;
  if (recipe?.kind === 'chip') {
    const chip = CHIPS.find(c => c.id === recipe.chipId);
    const dictEntry = chip && DICTIONARIES.find(d => d.id === chip.dictId);
    if (dictEntry) byos = findCardByName(dictEntry.cardName, cards);
  } else if (recipe?.kind === 'advanced') {
    byos = recipeToByos(recipe);
  }
  if (!byos) return null;
  const u = new URL(baseHref);
  u.hash = getBYOSID(byos, cards);
  return u.href;
}

// Build the self-contained byos.json bytes (with embedded custom
// corpus / twlist if present). Async, gzip+base64 is async. Returns
// { id, bytes, filename, mime } or throws.
async function buildShareableByosBytes() {
  const byos = getActiveByos();
  if (!byos) throw new Error('no active style');
  const id = getBYOSID(byos, cardsRegistry);
  const payload = { ...byos };
  if (customCorpus && customCorpus.text && customCorpus.text.length > 0) {
    payload.customCorpusData = {
      encoding: 'gzip+base64',
      name: customCorpus.name,
      data: await gzipBase64Encode(customCorpus.text),
    };
  }
  if (customTwlist && Array.isArray(customTwlist.entries) && customTwlist.entries.length > 0) {
    payload.customTwlistData = {
      encoding: 'gzip+base64',
      name: customTwlist.name,
      data: await gzipBase64Encode(JSON.stringify(customTwlist.entries)),
    };
  }
  const json = JSON.stringify(payload, null, 2) + '\n';
  const bytes = new TextEncoder().encode(json);
  return { id, bytes, filename: `${id}.json`, mime: 'application/json' };
}

