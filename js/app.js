// Builds the descriptor shown in source-meta (above the textarea):
// type indicator + source-of-content. Pure, no DOM access.
function buildSourceMeta(bytes, source) {
  if (source === null || bytes.length === 0) return 'No secret loaded';
  const type = bytesLooksLikeUtf8(bytes)
    ? 'Text'
    : 'Binary hexdump format (read-only)';
  let descriptor;
  switch (source) {
    case 'file':     descriptor = loadedFromFile ? `loaded from ${loadedFromFile}` : 'loaded from file'; break;
    case 'random':   descriptor = loadedFromFile || 'random bytes'; break;
    case 'revealed': descriptor = 'revealed from cover'; break;
    case 'pasted':   descriptor = 'pasted'; break;
    case 'typed':    descriptor = 'typed'; break;
    default:         descriptor = '';
  }
  return descriptor ? `${type} · ${descriptor}` : type;
}

async function updateSourceStats() {
  // The developer can type quickly; a stale hash promise might resolve
  // after a newer one. Track a request id and only apply the latest.
  const myId = ++lastStatsRequestId;
  const statsEl = $('source-stats');
  const metaEl  = $('source-meta');
  const bytes = loadedBytes ?? enc.encode($('source-text').value);

  // Above-textarea meta strip: type + source descriptor. Always
  // visible; reads 'No secret loaded' when buffer is empty.
  metaEl.textContent = buildSourceMeta(bytes, loadedSource);

  // Below-textarea stats strip: byte count + sha256. sha256 hidden
  // for the empty buffer (would always be the same well-known constant
  // and adds noise). For non-empty buffers, sha256 truncates with CSS
  // text-overflow when narrow.
  statsEl.textContent = `${bytes.length.toLocaleString()} bytes`;
  statsEl.removeAttribute('title');
  if (bytes.length === 0) return;
  try {
    const hash = await sha256Hex(bytes);
    if (myId !== lastStatsRequestId) return; // a newer update raced ahead
    statsEl.textContent = `${bytes.length.toLocaleString()} bytes · SHA-256: ${hash}`;
    statsEl.title = `SHA-256: ${hash}`;
  } catch {
    // crypto.subtle requires a secure context. Without it the line
    // stays at "N bytes" with no sha256.
    if (myId !== lastStatsRequestId) return;
  }
}

