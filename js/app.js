  const bytes = precomputedBytes != null
    ? precomputedBytes
    : enc.encode(t).length;
  let line = `${bytes.toLocaleString()} bytes`;
  if (coverSource === 'concealed' && lastConcealRatio != null && bytes > 0) {
    line += ` · story is ${formatRatio(lastConcealRatio)}× bigger than the secret`;
  }
