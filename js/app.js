// Auto-detect format and parse into [{type, word}, ...]. Recognized
// shapes: tab-delimited "type<TAB>word"; comma-delimited "type,word"
// (whitespace after the comma allowed); whitespace-delimited
// "type word". The per-file delimiter is locked from the first
// non-empty non-comment line; mixed-delimiter files surface a parse
// error on the offending line. Throws on malformed input, callers
// display the message in the Advanced validation row.
// Twlist textarea content is comma-delimited (`type,word` per row,
// `#`-prefixed comments allowed). The engine parser parseTwlistLines
// is whitespace-only and is the single canonical filter; we normalize
// commas to spaces before handing the text over so any change to the
// engine's word-boundary or rule-2 lexer round-trip propagates here
// for free. Returns { entries: [{type, word, lineIndex}], rejections },
// same shape as parseTwlistLines with reportRejections.
function parseTwlistTextareaContent(text) {
  const normalized = text.replace(/,/g, ' ');
  // _redactedSingles is preloaded at module bottom via top-level await
  // from getRedactedSingles(). When present, lines whose canonical
  // lowercase word is in the set are rejected with reason 'redacted'
  // (alongside 'malformed' / 'lexer-rejected') so the UI validation row
  // shows the user WHY their entry was dropped. Defense-in-depth at
  // sortDict still applies regardless.
  return parseTwlistLines(normalized, {
    reportRejections: true,
    redactedSingles: _redactedSingles,
  });
}

