async function handleIngressedSourceFiles(fileList) {
  if (!fileList || fileList.length === 0) return;
  const files = Array.from(fileList);
  if (!shouldUsePipeline(files, 'encode')) {
    await loadSourceFromFile(files[0]);
    return;
  }
  const choice = await requestLargeFileFlow({ files, kind: 'encode' });
  if (choice === 'cancel') return;
  if (choice === 'loadbox') { await loadSourceFromFileWithModal(files[0]); return; }
  if (choice === 'pipeline') await runPipeline({ files, kind: 'encode' });
}
