    // present in encode pipeline mode, even with envelope='none' and
    // an empty stack, the filename prefix infix and Save-Cover-style
    // extension default (".txt") still apply uniformly:
    // outName = `${f.name}.${basename}${filenameSuffix}`.
    spec.wrap = {
      layers: wrapBuildLayers(),
      basename: effectiveFilename(),
      filenameSuffix: wrapOutputName().slice(effectiveFilename().length),
    };
