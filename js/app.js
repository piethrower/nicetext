// Cover bytes (true UTF-8 byte count) + optional concealment ratio.
// Bytes-only after paste/load (no secret to compare to); bytes ·
// ratio after a successful Conceal. 0 bytes when empty.
//
// Optional `precomputedBytes` skips the TextEncoder.encode(text)
// step when the caller already has the byte count (Conceal does, it
